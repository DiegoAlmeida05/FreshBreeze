const getDayOfYear = (value: Date) => {
  const startOfYear = new Date(value.getFullYear(), 0, 1)
  const currentDay = new Date(value.getFullYear(), value.getMonth(), value.getDate())
  const oneDayInMs = 24 * 60 * 60 * 1000

  return Math.floor((currentDay.getTime() - startOfYear.getTime()) / oneDayInMs) + 1
}

export const getDailyQuoteForDate = (value: Date = new Date()) => {
  const dayOfYear = getDayOfYear(value)
  const quoteIndex = (dayOfYear - 1) % dailyQuotes.length
  return dailyQuotes[quoteIndex]
}